package com.example.common.entity;

/**
 * Роли пользователей в системе.
 * Используется для авторизации через Keycloak.
 */
public enum ERole {
    /** Администратор — полный доступ ко всем функциям */
    ROLE_ADMIN,
    /** Менеджер — управление заказами и клиентами */
    ROLE_MANAGER,
    /** Производство — управление этапами производства */
    ROLE_PRODUCTION,
    /** Бухгалтер — управление оплатами, просмотр аналитики */
    ROLE_ACCOUNTANT,
    /** Обычный пользователь — базовый доступ */
    ROLE_USER
}
